static inline const uint8_t *get_ascii_char_font_const(char c)
{
    unsigned char uc = (unsigned char)c;

    if (uc < ASCII_FONT_FIRST || uc > ASCII_FONT_LAST) {
        uc = '?';
    }

    return ascii_font_8x16[uc - ASCII_FONT_FIRST];
}

/*
 * Compatibility with code like:
 *
 *     uint8_t *char_format = get_ascii_char_font(c);
 *
 * The returned pointer is a static 16-byte buffer and is overwritten on each call.
 */
static inline uint8_t *get_ascii_char_font(char c)
{
    static uint8_t out[ASCII_FONT_HEIGHT];
    const uint8_t *src = get_ascii_char_font_const(c);

    for (int i = 0; i < ASCII_FONT_HEIGHT; i++) {
        out[i] = src[i];
    }

    return out;
}
